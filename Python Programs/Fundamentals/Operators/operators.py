#arithmetic operators
a,b=map(int,input().split())

print("Addition:", a+b)
print("Subtraction:", a-b)
print("multiplication:", a*b)
print("division:", a/b)
print("floor division:", a//b)
print("modulus:", a%b)
print("exponential:", a**b)

#comparison operators
print(a>b)
print(a<b)
print(a==b)
print(a != b)
print(a >= b)
print(a <= b)

#logical operators
a,b=True, False
print(a and b)
print(a or b)
print(not a)

#bitwise operators
a,b=map(int,input().split())
print(a & b)
print(a | b)
print(~a)
print(a^b)
print(a)






